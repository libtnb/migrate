func TestTableCommentAndRenameIndex(t *testing.T) {
	declare := func(s *Schema) {
		s.Create("events", func(t *Table) {
			t.ID()
			t.Comment("it's the event log")
		})
		s.Table("events", func(t *Table) {
			t.RenameIndex("events_at_index", "events_at_idx")
			t.Comment("updated")
		})
	}
	assertSQL(t, compileSchema(t, Postgres, declare), []string{
		`CREATE TABLE "events" (
	"id" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY
)`,
		`COMMENT ON TABLE "events" IS 'it''s the event log'`,
		`ALTER INDEX "events_at_index" RENAME TO "events_at_idx"`,
		`COMMENT ON TABLE "events" IS 'updated'`,
	})
	assertSQL(t, compileSchema(t, MySQL, declare), []string{
		"CREATE TABLE `events` (\n\t`id` BIGINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY\n) COMMENT = 'it''s the event log'",
		"ALTER TABLE `events` RENAME INDEX `events_at_index` TO `events_at_idx`",
		"ALTER TABLE `events` COMMENT = 'updated'",
	})

	// SQLite ignores comments and refuses index renames explicitly.
	s := &Schema{}
	s.Create("events", func(t *Table) {
		t.ID()
		t.Comment("ignored")
	})
	stmts, err := SQLite.compile(s.ops[0])
	if err != nil || len(stmts) != 1 {
		t.Fatalf("sqlite create with comment = %v, %v", stmts, err)
	}
	s = &Schema{}
	s.Table("events", func(t *Table) { t.RenameIndex("a", "b") })
	if _, err := SQLite.compile(s.ops[0]); err == nil || !strings.Contains(err.Error(), "cannot rename index") {
		t.Fatalf("sqlite rename index should error clearly, got: %v", err)
	}

	// Renaming an index reverses to the opposite rename; changing a comment
	// in an alteration is irreversible.
	m := migrationOf(t, func(s *Schema) {
		s.Table("events", func(t *Table) { t.RenameIndex("a", "b") })
	})
	assertSQL(t, downSQL(t, Postgres, m), []string{`ALTER INDEX "b" RENAME TO "a"`})
	m = migrationOf(t, func(s *Schema) {
		s.Table("events", func(t *Table) { t.Comment("x") })
	})
	if _, err := m.downOps(); !errors.Is(err, ErrIrreversible) {
		t.Fatalf("comment change should be irreversible, got: %v", err)
	}
}

func TestAutoIncrementColumns(t *testing.T) {
	declare := func(s *Schema) {
		s.Create("counters", func(t *Table) {
			t.Integer("id").AutoIncrement()
			t.String("name")
		})
	}
	assertSQL(t, compileSchema(t, Postgres, declare), []string{
		`CREATE TABLE "counters" (
	"id" INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	"name" VARCHAR(255) NOT NULL
)`,
	})
	assertSQL(t, compileSchema(t, MySQL, declare), []string{
		"CREATE TABLE `counters` (\n\t`id` INT NOT NULL AUTO_INCREMENT PRIMARY KEY,\n\t`name` VARCHAR(255) NOT NULL\n)",
	})
	assertSQL(t, compileSchema(t, SQLite, declare), []string{
		`CREATE TABLE "counters" (
	"id" INTEGER PRIMARY KEY AUTOINCREMENT,
	"name" VARCHAR(255) NOT NULL
)`,
	})

	// SmallInteger keeps its width on engines that distinguish it.
	small := compileSchema(t, Postgres, func(s *Schema) {
		s.Create("c", func(t *Table) { t.SmallInteger("id").AutoIncrement() })
	})
	if !strings.Contains(small[0], `"id" SMALLINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY`) {
		t.Errorf("smallint identity, got: %s", small[0])
	}
}

