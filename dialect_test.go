func TestSQLiteRejectsConstraintAlterations(t *testing.T) {
	cases := map[string]func(*Table){
		"add foreign":  func(t *Table) { t.Foreign("org_id").References("orgs") },
		"drop foreign": func(t *Table) { t.DropForeign("team_id") },
		"add primary":  func(t *Table) { t.Primary("a", "b") },
		"drop primary": func(t *Table) { t.DropPrimary() },
	}
	for name, fn := range cases {
		t.Run(name, func(t *testing.T) {
			s := &Schema{}
			s.Table("users", fn)
			_, err := SQLite.compile(s.ops[0])
			if err == nil {
				t.Fatal("expected an explicit error, got none")
			}
			if !strings.Contains(err.Error(), "sqlite cannot") {
				t.Fatalf("error should explain the SQLite limitation, got: %v", err)
			}
		})
	}
}

func TestRenameAndDropCompile(t *testing.T) {
	got := compileSchema(t, Postgres, func(s *Schema) {
		s.Rename("old_users", "users")
		s.Drop("legacy")
		s.DropIfExists("maybe")
	})
	want := []string{
		`ALTER TABLE "old_users" RENAME TO "users"`,
		`DROP TABLE "legacy"`,
		`DROP TABLE IF EXISTS "maybe"`,
	}
	assertSQL(t, got, want)

	got = compileSchema(t, MySQL, func(s *Schema) { s.Rename("a", "b") })
	assertSQL(t, got, []string{"RENAME TABLE `a` TO `b`"})
}

