func TestPostgresAlterTable(t *testing.T) {
	got := compileSchema(t, Postgres, func(s *Schema) {
		s.Table("users", func(t *Table) {
			t.String("nickname", 50).Nullable().Comment("display name")
			t.RenameColumn("name", "full_name")
			t.DropColumn("bio")
			t.Index("nickname")
			t.DropUnique("email")
			t.Foreign("org_id").References("orgs").NullOnDelete()
			t.DropForeign("team_id")
		})
	})
	want := []string{
		`ALTER TABLE "users" ADD COLUMN "nickname" VARCHAR(50)`,
		`COMMENT ON COLUMN "users"."nickname" IS 'display name'`,
		`ALTER TABLE "users" RENAME COLUMN "name" TO "full_name"`,
		`ALTER TABLE "users" DROP COLUMN "bio"`,
		`CREATE INDEX "users_nickname_index" ON "users" ("nickname")`,
		`DROP INDEX "users_email_unique"`,
		`ALTER TABLE "users" ADD CONSTRAINT "users_org_id_foreign" FOREIGN KEY ("org_id") REFERENCES "orgs" ("id") ON DELETE SET NULL`,
		`ALTER TABLE "users" DROP CONSTRAINT "users_team_id_foreign"`,
	}
	assertSQL(t, got, want)
}

