// compileSchema runs fn against a fresh Schema and compiles the recorded
// operations for d, returning the SQL texts. Opaque fn statements render as
// "-- " plus their description, so golden lists document them in place.
func compileSchema(t *testing.T, d Dialect, fn func(*Schema)) []string {
	t.Helper()
	s := &Schema{}
	fn(s)
	if err := validateSchema("test", s); err != nil {
		t.Fatalf("declaration error: %v", err)
	}
	var out []string
	for _, op := range s.ops {
		stmts, err := d.compile(op)
		if err != nil {
			t.Fatalf("compile: %v", err)
		}
		for _, st := range stmts {
			if st.fn != nil {
				out = append(out, "-- "+describeStatement(st))
				continue
			}
			out = append(out, st.sql)
		}
	}
	return out
}

func assertSQL(t *testing.T, got []string, want []string) {
	t.Helper()
	if len(got) != len(want) {
		t.Fatalf("statement count = %d, want %d\ngot:\n%s", len(got), len(want), strings.Join(got, "\n---\n"))
	}
	for i := range want {
		if got[i] != want[i] {
			t.Errorf("statement %d:\n got: %s\nwant: %s", i+1, got[i], want[i])
		}
	}
}

// The showcase table exercises most column kinds, modifiers, conventional
// index names and the constrained foreign key in one declaration.
func showcase(s *Schema) {
	s.Create("users", func(t *Table) {
		t.ID()
		t.String("email").Unique()
		t.String("name", 100)
		t.Text("bio").Nullable()
		t.Boolean("active").Default(true)
		t.Decimal("balance", 10, 2).Default(0)
		t.Enum("role", "admin", "member").Default("member")
		t.JSON("settings").Nullable()
		t.ForeignID("team_id").Constrained().CascadeOnDelete()
		t.Timestamps()
		t.Index("name", "active")
	})
}

func TestPostgresCreateTable(t *testing.T) {
	got := compileSchema(t, Postgres, showcase)
	want := []string{
		`CREATE TABLE "users" (
	"id" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	"email" VARCHAR(255) NOT NULL,
	"name" VARCHAR(100) NOT NULL,
	"bio" TEXT,
	"active" BOOLEAN NOT NULL DEFAULT TRUE,
	"balance" NUMERIC(10, 2) NOT NULL DEFAULT 0,
	"role" VARCHAR(255) NOT NULL DEFAULT 'member' CONSTRAINT "users_role_check" CHECK ("role" IN ('admin', 'member')),
	"settings" JSONB,
	"team_id" BIGINT NOT NULL,
	"created_at" TIMESTAMPTZ,
	"updated_at" TIMESTAMPTZ,
	CONSTRAINT "users_team_id_foreign" FOREIGN KEY ("team_id") REFERENCES "teams" ("id") ON DELETE CASCADE
)`,
		`CREATE UNIQUE INDEX "users_email_unique" ON "users" ("email")`,
		`CREATE INDEX "users_name_active_index" ON "users" ("name", "active")`,
	}
	assertSQL(t, got, want)
}

