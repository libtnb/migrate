func TestMySQLBackslashEscaping(t *testing.T) {
	got := compileSchema(t, MySQL, func(s *Schema) {
		s.Create("t", func(t *Table) {
			t.String("path").Default(`C:\logs`)
		})
	})
	want := []string{
		"CREATE TABLE `t` (\n\t`path` VARCHAR(255) NOT NULL DEFAULT 'C:\\\\logs'\n)",
	}
	assertSQL(t, got, want)

	// Postgres keeps backslashes verbatim (standard_conforming_strings).
	got = compileSchema(t, Postgres, func(s *Schema) {
		s.Create("t", func(t *Table) {
			t.String("path").Default(`C:\logs`)
		})
	})
	if !strings.Contains(got[0], `DEFAULT 'C:\logs'`) {
		t.Fatalf("postgres should not double backslashes, got: %s", got[0])
	}
}

